/* RetroAchievements on-screen feedback. When set to 1, the Gecko C0 VBlank
 * hook injected below is the EXTENDED variant: besides advancing the frame
 * counter at 0xC0002FF8, it draws a 32x32 trophy badge (white box + black
 * rounded border + gold trophy) in the top-left of the active XFB whenever
 * the trophy flag at 0xC0002FFC is non-zero (the d2x ra-module sets it on an
 * achievement unlock, blinking 3x in sync with the disc-slot LED). Drawn into
 * both the current and previous VI_TFBL to survive double buffering. Set to 0
 * to fall back to the counter-only hook — both variants are independently safe
 * (a stale flag is simply never read).
 * The d2x side has a matching RA_TROPHY_OVERLAY toggle in ra-module/main.c. */
#define RA_TROPHY_OVERLAY	1

/* DEBUG: when 1 (and RA_TROPHY_OVERLAY is 1), inject a test hook that draws
 * the trophy badge gated on the VBI COUNTER (0x2FF8 bit6) instead of the
 * unlock flag — so it blinks continuously without needing an achievement.
 * Used to validate the draw/visibility independent of Starlet flag delivery.
 * Set to 0 for the real flag-gated overlay (only on unlock). */
#define RA_TROPHY_TEST_ALWAYS	0

